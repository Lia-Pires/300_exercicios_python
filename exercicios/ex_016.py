"""
16 - Escreva um programa que pede que o usuário dê entrada em dois valores, em seguida, exiba em tela o resultado da soma, subtração, multiplicação e divisão desses números:
"""

num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

print(f"Os números digitados foram: {num1} e {num2}.")
print(f"Sua soma é {num1 + num2}.")
print(f"Sua subtração é {num1 - num2}.")
print(f"Sua multiplicação é: {num1 * num2}.")
print(f"E sua divisão é: {num1 / num2}.")
